public class Calculadora{
    public int x,y;

    public static int suma(int x, int y){
        return x+y;
	}

	public static int resta(int x, int y){
        return  x - y;
    }
    
    public static int mul(int x, int y){
        return x * y;
    }

    public static int div(int x, int y){
        return x / y;
    }
}