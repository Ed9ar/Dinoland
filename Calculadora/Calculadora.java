public class Calculadora{
    public int x,y;

    public static double suma(int x, int y){
        return x+y;
	}

	public static double resta(int x, int y){
        return  x - y;
    }
    
    public static double mul(int x, int y){
        return x * y;
    }

    public static double div(int x, int y){
        return x / y;
    }
}
